shopping_list = ["apple","blueberry","peach"]
print("melon 을 끝에 추가하기")
shopping_list.append("melon")
print(shopping_list)

print("리스트의 마지막 물품을 삭제하기")
shopping_list.pop()
print(shopping_list)

print("melon 을 가장 처음에 추가하기")
shopping_list.insert(0, "melon")
print(shopping_list)

print("apple 을 삭제하기")
shopping_list.remove("apple")
print(shopping_list)

print("두번째에 있는 물품을 삭제하기")
shopping_list.pop(1)
print(shopping_list)

print("리스트를 출력하시오")
print(shopping_list)
